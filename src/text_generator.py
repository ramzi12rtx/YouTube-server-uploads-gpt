import random

def generate_script():
    topics = [
        "هل تعلم أن النوم الجيد يمكن أن يزيد من إنتاجيتك بشكل كبير؟",
        "أغرب 5 حقائق علمية لا يصدقها العقل!",
        "لماذا يجب أن تتعلم الذكاء الاصطناعي في 2025؟",
        "نصيحة واحدة يمكن أن تغيّر حياتك المالية للأفضل!",
        "أسرار لم يخبرك بها أحد عن النجاح الشخصي!",
        "كيف تخطط يومك بطريقة ذكية؟",
        "تعرف على هذه المهارة التي تجعل منك محترفًا خلال شهر!",
        "هذا الخطأ الصغير قد يكون سبب فشلك في العمل أو الدراسة!",
        "هل الهاتف يسبب تراجع التركيز؟ اكتشف الحقيقة!",
        "لماذا ينجح البعض بسرعة ويفشل الآخرون؟"
    ]

    introductions = [
        "مرحباً بك في قناتنا 👋",
        "أهلاً بك في فيديو جديد 🎥",
        "استعد لمعلومة قد تغير تفكيرك 🧠",
        "جاهز لاكتشاف شيء مذهل؟ 🚀",
        "في هذا الفيديو سنكشف لك سر مهم 🔍"
    ]

    closings = [
        "إذا أعجبك الفيديو لا تنس الإعجاب والاشتراك ✨",
        "اكتب رأيك في التعليقات 👇",
        "تابعنا للمزيد من هذه المواضيع 👀",
        "شارك هذا الفيديو مع أصدقائك ❤️",
        "هل تريد جزء ثاني؟ اكتب لنا! 🔁"
    ]

    # توليد السكربت باحترافية:
    intro = random.choice(introductions)
    topic = random.choice(topics)
    closing = random.choice(closings)

    script = f"{intro}\n{topic}\n{closing}"
    return script
